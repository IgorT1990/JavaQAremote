package ua.hillel.homework.lesson3;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 8;
        if (number % 2 == 0){
            System.out.println("Even Number");
        }
        else {
            System.out.println("Odd Number");
        }
        System.out.println("BYE");
        System.exit(number);
    }
}
